/**
 * add - fun op
 * @a: par1
 * @b: par2
 *
 * Return: Always 0.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - fun op
 * @a: par1
 * @b: par2
 *
 * Return: Always 0.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - fun op
 * @a: par1
 * @b: par2
 *
 * Return: Always 0.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - fun op
 * @a: par1
 * @b: par2
 *
 * Return: Always 0.
 */
int div(int a, int b)
{
	if (b == 0)
		return (0);
	return (a / b);
}
/**
 * mod - fun op
 * @a: par1
 * @b: par2
 *
 * Return: Always 0.
 */
int mod(int a, int b)
{
	if (b == 0)
		return (0);
	return (a % b);
}

